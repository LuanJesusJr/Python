
# Um conversor de temperatura de C° para F°
# Criar um programa onde receba a temperatura em C°
# E converta de forma automatica para F°

print('\n')
print('CONVERSOR DE C° PARA FAHRENHEIT :)\n ')

n1 = int(input('Digite a temperatura em C°: '))
print('\n')

s = n1 * 9 / 5 + 32

print('A temperatura em Fahrenheit é: {}'.format(s ))
print('\n')